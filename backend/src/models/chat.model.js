// Import Section
import mongoose from "mongoose";

// Schema Section
const chatSchema = new mongoose.Schema(
  {
    sender: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    receiver: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

// Exporting and Creating Model Section
const Chat = mongoose.model("Chat", chatSchema);
export { Chat };
